                    <h3 tabindex="0" class=" text-center animacaoh1">Era da animação computadorizada</h3>
                    <p class=" text-justify">A Era da animação computadorizada ou era da nova animação ou era da
                        computação gráfica ou também Período da animação por computador foi como ficou conhecido o
                        período que vai do início da Década de 80 aos anos 2000, onde vários longas ou curtas feitos
                        por gráficos de computadores foram produzidos em vários países. Esse período também marcou o
                        começou da chamada era da informática. Durante esses anos estúdios de animação como a Walt
                        Disney Pictures, Paramount Pictures, Pixar Animation Studios, Blue Sky Studios, DreamWorks
                        SKG entre outros dedicaram-se a fazer amostras produzidas por CGI (imagens feitas por
                        computador) mesmo sendo dificil, pois naquela época era dificil achar material suficiente
                        para fazer uma animação feita por computação gráfica. A primeira mostra lançada foi The
                        Adventures of André & Wally B. em 1984, dirigida por John Lasseter. O programa de computador
                        usado para renderizar o curta criava apenas figuras geométricas em 3D. Por essa causa, se
                        você observar com atenção, o corpo dos personagens são compostos de esferas, cones,
                        cilindros, elipsoides, etc. Mas o primeiro longa-metragem lançado foi Toy Story em 1995 com
                        qual outros estúdios viram que era possível fazer filme com computação. Após isso houve
                        outras tentativas de lançamentos de filmes, de 1996 até 2000 foram feitas sete tentativas,
                        contudo nenhuma deu certo devido a pouco material.</p>
