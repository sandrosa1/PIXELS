                        de ilusão de movimento. Em 1962 Ivan Sutherland desenvolveu uma “caneta óptica”
                        com a qual imagens poderiam ser esboçadas em um computador.
                        O primeiro programa feito por Sutherland que permitia a realização de projetos
                        com a ajuda de computador, chamado Sketchpad, permitia que os projetistas usassem
                        computadores para criar projetos de automóveis, cidades e produtos indústriais.
                        A imagem mostra Ivan Sutherland demonstrando o sistema “Sketchpad” no
                        console do imenso computador TX-2 no MIT em 1963. O grande “salto” da computação gráfica
                        foi com o surgimento da tecnologia de circuitos integrados e, posteriormente, na
                        década de 80, o lançamento dos primeiros computadores pessoais de baixo custo, como
                        o IBM-PC e o Macintosh da Apple. Em meados da década de 80, o novo terminal gráfico
                        de varredura (raster graphics displays) foi a tecnologia que popularizou de vez o uso
                        de gráficos nos computadores pessoais, tecnologia esta que possibilitou o desenvolvimento
                        de inúmeros aplicativos baratos e fáceis de usar, tais como processadores de textos e planilhas
                        eletrônicas. Nessas interfaces gráficas, o usuário conseguia posicionar janelas que atuavam como
                        terminais virtuais, cada qual executando aplicativos independentes, permitindo assim que ele
                        executasse vários aplicativos simultaneamente. O aprimoramento da tecnologia raster e o
                        surgimento de
                        placas aceleradoras gráficas permitiu na década de 90 a geração de imagens com alto grau
                        de realismo em tempos real, favorecendo assim o surgimento de novas tecnologias nas áreas
                        de jogos e efeitos especiais.</p>
